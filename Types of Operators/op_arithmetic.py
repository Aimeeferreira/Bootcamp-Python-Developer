''' 
In mathematics, the order is as follows:

- Parentheses
- Exponents
- Multiplication and division (from left to right)
- Addition and subtraction (from left to right)
'''

# Simple usage examples:

product_1 = 20
product_2 = 10

print(product_1 +  product_2)
print(product_1 -  product_2)
print(product_1 /  product_2)
print(product_1 // product_2)
print(product_1 %  product_2)
print(product_1 *  product_2)
print(product_1 ** product_2)

x = 10 + 5 * 4
print(x)


z = (10 + 5) * 4
print(z)


y = (10 / 2) + (25 * 2) - (2 ** 2)
print(y)
