''' 
Na matemática segue-se a ordem:

- Parênteses
- Expoentes
- Multiplicações e divisões (da esquerda para a direita)
- Somas e subtrações (da esquerda para a direita)

'''

# Exemplos simples de utilização:

produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2)
print(produto_1 - produto_2)
print(produto_1 / produto_2)
print(produto_1 // produto_2)
print(produto_1 % produto_2)
print(produto_1 * produto_2)
print(produto_1 ** produto_2)

x = 10 + 5 * 4
print(x)


x = (10 + 5) * 4
print(x)


y = (10 / 2) + (25 * 2) - (2 ** 2)
print(y)